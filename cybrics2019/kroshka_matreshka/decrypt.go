
package main

import "fmt"
import "crypto/rc4"


func main() {
    key := []byte{0x1, 0x3, 0x3, 0x7, 0x3, 0x3, 0x8, 0x1}
    src := []byte{0x53, 0xdd, 0xc5, 0x87, 0xe4, 0x63, 0x99, 0x14, 0x4f, 0xa4, 0x14, 0x2d, 0xc4, 0x24, 0x4, 0xc0, 0xb0}

    c, _ := rc4.NewCipher(key)

    c.XORKeyStream(src, src)
    dec := string(src)
    fmt.Printf("%s\n", dec)
}
